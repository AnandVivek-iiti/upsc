// ─── Pure helper functions ────────────────────────────────────────────────────

export function getPct(modules) {
  const vals = Object.values(modules);
  if (!vals.length) return 0;
  return Math.round(vals.reduce((s, m) => s + m.progress, 0) / vals.length);
}

export function deepClone(obj) {
  return JSON.parse(JSON.stringify(obj));
}

export function getAllModules(data, stage) {
  return Object.values(data[stage]).flatMap((p) => Object.values(p.modules));
}